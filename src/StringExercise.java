public class StringExercise {
    public static void main(String[] args) {
        String word = "We don't need no education\nWe don't need no thought control\n";

        word += "\nCheck \"this\" out!, \"s inside of \"s!\n";

        word += "\nIn windows, the main drive is usually in C:\\";

        word += "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(word);

    }
}
